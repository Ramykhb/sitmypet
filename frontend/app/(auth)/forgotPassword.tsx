import api from "@/config/api";
import { router } from "expo-router";
import * as SecureStore from "expo-secure-store";
import React, { useState } from "react";
import {
  ActivityIndicator,
  Image,
  Keyboard,
  Text,
  TextInput,
  TouchableOpacity,
  TouchableWithoutFeedback,
  View,
} from "react-native";
import { KeyboardAwareScrollView } from "react-native-keyboard-aware-scroll-view";
import { SafeAreaView } from "react-native-safe-area-context";

export default function OtpInput({
  onChange,
}: {
  onChange?: (otp: string) => void;
}) {
  const [email, setEmail] = useState("");
  const [error, setError] = useState("");
  const [loading, setLoading] = useState(false);

  const handleSubmit = async () => {
    if (!loading) {
      setError("");
      setLoading(true);
      try {
        await api.post(`/auth/request-password-reset`, {
          email: email,
        });
        await SecureStore.setItemAsync("passResetEmail", email);
        router.push("/(auth)/verifyPasswordReset");
      } catch (error: any) {
        setError("Email not found.");
      } finally {
        setLoading(false);
      }
    }
  };

  return (
    <SafeAreaView
      className="home-auth flex-1"
      edges={["right", "left", "bottom"]}
    >
      <TouchableWithoutFeedback onPress={Keyboard.dismiss}>
        <KeyboardAwareScrollView
          contentContainerStyle={{ flexGrow: 1 }}
          enableOnAndroid={true}
          extraScrollHeight={35}
          keyboardOpeningTime={100}
          keyboardShouldPersistTaps="handled"
        >
          <View className="flex flex-col flex-1 w-full p-10 items-center">
            <Image
              source={require("../../assets/images/forgot-password.png")}
              alt="logo"
              className="w-full h-64"
              resizeMode="contain"
            />
            <Text className="text-[#0A0A0A] text-lg mt-5 font-bold">
              Reset Password
            </Text>
            <Text className="text-gray-500 text-base mt-5 font-bold text-center">
              Please enter your email to reset your password.
            </Text>

            <TextInput
              className={
                "w-full h-14 border border-gray-300 rounded-xl mt-8 px-5"
              }
              autoCapitalize={"none"}
              keyboardType="email-address"
              autoComplete="off"
              textContentType="none"
              importantForAutofill="no"
              returnKeyType="next"
              onChangeText={(text) => {
                setEmail(text);
              }}
            />

            <Text className={"text-base text-red-600 my-2"}>{error}</Text>

            <TouchableOpacity
              className="w-[75%] bg-[#3944D5] h-16 rounded-full flex flex-row items-center justify-center my-8"
              onPress={handleSubmit}
            >
              {loading ? (
                <ActivityIndicator color={"#FFFFFF"} size={"small"} />
              ) : (
                <Text className="text-white text-lg font-bold">Submit</Text>
              )}
            </TouchableOpacity>
          </View>
        </KeyboardAwareScrollView>
      </TouchableWithoutFeedback>
    </SafeAreaView>
  );
}
